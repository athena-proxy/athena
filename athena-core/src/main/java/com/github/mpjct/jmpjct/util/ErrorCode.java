    private static String convert2Identifier(ErrorCode errorCode) {
        String errorCodeName = errorCode.name();
        if (errorCodeName.startsWith("PG_")) {
            return errorCode.sqlState;
        } else if (errorCodeName.startsWith("ER_") || errorCodeName.startsWith("WARN_")) {
            return String.valueOf(errorCode.errorNo);
        } else {
            return errorCodeName;
        }
    }

    private static String convert2ProtoName(ErrorCode errorCode) {
        String errorCodeName = errorCode.name();
        if (!errorCodeName.startsWith("PG_")) {
            return errorCodeName;
        }
        if (errorCodeName.endsWith("_CLASS")) {
            return errorCodeName.substring(3, errorCodeName.length() - 9);
        }
        return errorCodeName.substring(3);
    }

    private static final Map<Integer, ErrorCode> DBERR_MYSQL_MAP = new HashMap<>();
    private static final Map<String, ErrorCode> DBERR_PG_MAP = new HashMap<>();
    private static final Logger logger = LoggerFactory.getLogger(ErrorCode.class);

    static {
        for (Field field : ErrorCode.class.getDeclaredFields()) {
            if (field.getName().startsWith("ER_") || field.getName().startsWith("WARN_")) {
                try {
                    ErrorCode DBERR = valueOf(field.getName());
                    DBERR_MYSQL_MAP.put(DBERR.getErrorNo(), DBERR);
                } catch (Exception e) {
                    logger.error(Objects.toString(e));
                }
            }
            if (field.getName().startsWith("PG_")) {
                try {
                    ErrorCode DBERR = valueOf(field.getName());
                    DBERR_PG_MAP.put(DBERR.getSqlState(), DBERR);
                } catch (Exception e) {
                    logger.error(Objects.toString(e));
                }
            }
        }
    }

    public static ErrorCode getDBERRByErrorNo(int errorNo) {
        ErrorCode DBERR = DBERR_MYSQL_MAP.get(errorNo);
        if (DBERR == null) {
            return ERR_UNRECOGNIZED_ERR_COED;
        }
        return DBERR;
    }

    public static ErrorCode getPGDBERRBySqlState(String sqlState) {
        ErrorCode DBERR = DBERR_PG_MAP.get(sqlState);
        if (DBERR == null) {
            return ERR_UNRECOGNIZED_ERR_COED;
        }
        return DBERR;
    }

